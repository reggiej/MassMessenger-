package com.acnc.mm.domain.messenger;

import java.io.Serializable;

public class UpdateContract  implements Serializable{
	
	
	
	 /**
	 * 
	 */
	private static final long serialVersionUID = 5313925581855529917L;

	public Long contractID;
	 
	 public String contractTitle;
		
	 public String contractAgency;
	 
	 
	 public UpdateContract(){
			
		}
	
	 
	 public UpdateContract(Long contractID, String contractTitle, String contractAgency) {
		 
		 this.contractID = contractID;
		 this.contractTitle = contractTitle;
		 this.contractAgency = contractAgency;
	 }


	public Long getContractID() {
		return contractID;
	}


	public void setContractID(Long contractID) {
		this.contractID = contractID;
	}


	public String getContractTitle() {
		return contractTitle;
	}


	public void setContractTitle(String contractTitle) {
		this.contractTitle = contractTitle;
	}


	public String getContractAgency() {
		return contractAgency;
	}


	public void setContractAgency(String contractAgency) {
		this.contractAgency = contractAgency;
	}
		
	 
}
